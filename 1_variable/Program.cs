using System;

namespace _1_variable
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Hello Variable");

            int a;
            int c, d;

            // Whole number
            int integer; // 32bit
            short shorter; // 16bit
            long longer; // 64bit

            uint unsign_integer;

            // Decimal Number
            float f;
            f = 5.234F; // add suffix to convert double to float

            double dou;
            dou = 5.234;

            decimal dec;
            dec = 4.43211M;

            // Boolean (true, false)
            bool boo; boo = true;

            // Character
            char ch; ch = 'C';
            string str; str = "String";

        }
    }
}
